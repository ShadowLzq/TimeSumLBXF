public class Happy {
    private int id;
    private String happyName;
    private double happyTime;

    public void setId(int id) {
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setHappyName(String happyName) {
        this.happyName = happyName;
    }

    public String getHappyName() {
        return happyName;
    }

    public void setHappyTime(double happyTime) {
        this.happyTime = happyTime;
    }

    public double getHappyTime() {
        return happyTime;
    }
}
